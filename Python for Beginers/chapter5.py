'''
Chapter 1 - Variables

Variables types:

str = string
int = integer
float = number with decimal place
bool = boolean(true or false)

'''

age1 = int(20)
age2 = int(10)
sum = age1 + age2
average = sum/2
print(f"the average of ages 1 and 2 is: {average}")
#a division always returns a float type number

print(type(average))
#the type function allows us to know the type of the variables