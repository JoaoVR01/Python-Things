'''
Write three programs: (i) a for loop that prints the numbers one by one, from 1 to 80. 
(ii) a for loop that prints the numbers from 1000 to 0 (decrementing by 100). 
And (iii) a for loop that prints the even numbers from 2 to 66.
'''

# program i:
for i in range(1,81):
    print(i)
print("----------------------------------")
# program ii:
for i in range(1000, -1, -100):
    print(i)
print("----------------------------------")
# program iii:
for i in range(2,67,2):
    print(i)